    private void novoButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_novoButtonActionPerformed
        String tipoUsuario = tipoTextField.getText();
        String loginUsuario = loginTextField.getText();
        String senhaUsuario = senhaPasswordField.getText();
        String nomeUsuario = nomeTextField.getText();
        String cpfUsuario = cpfTextField.getText();
        
        if(nomeUsuario == null || nomeUsuario.length() <= 2
                || loginUsuario == null || loginUsuario.length() <= 3 || senhaUsuario == null || senhaUsuario.length() < 6
                || cpfUsuario.equals("00000000000") || cpfUsuario.equals("11111111111") || cpfUsuario.equals("22222222222") 
                || cpfUsuario.equals("33333333333") || cpfUsuario.equals("44444444444") || cpfUsuario.equals("55555555555") 
                ||cpfUsuario.equals("66666666666") || cpfUsuario.equals("77777777777") ||cpfUsuario.equals("88888888888") 
                || cpfUsuario.equals("99999999999") ||
                (cpfUsuario.length() != 11) || !tipoUsuario.equals("adm") && !tipoUsuario.equals("usr")){
            
            JOptionPane.showMessageDialog(null, "Verifique os campos e tente novamente...");
        }else{
            try{
                int escolha = JOptionPane.showConfirmDialog(null, "Confirmar cadastro de novo " + tipoUsuario + "?","Adicionar novo usuário", JOptionPane.YES_NO_OPTION);
                switch (escolha){
                    case JOptionPane.YES_OPTION:
                        UsuarioGerenciar usuariogerenciar = new UsuarioGerenciar(tipoUsuario, loginUsuario, senhaUsuario, nomeUsuario, cpfUsuario);
                        DAO dao = new DAO();
                        dao.inserirUsuario(usuariogerenciar);
                        JOptionPane.showMessageDialog(null, "Usuário inserido com sucesso!");
                        buscarUsuarios();
                        break;
                    default:
                        JOptionPane.showMessageDialog(null, "A inserção não será feita.");
                        break;
                }
            }
            catch (Exception e){
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, "Sistema indisponível, tente novamente mais tarde...");
            }
        }  
    }//GEN-LAST:event_novoButtonActionPerformed

