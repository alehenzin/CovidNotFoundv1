    private void atualizarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_atualizarButtonActionPerformed
        int id = Integer.parseInt(idTextField.getText());
        String tipo = tipoTextField.getText();
        String login = loginTextField.getText();
        String senha = senhaPasswordField.getText();
        String nome = nomeTextField.getText();
        String cpf = cpfTextField.getText();
        
        
        if(nome == null || nome.length() <= 2
                || login == null || login.length() <= 3 || senha == null || senha.length() < 6
                || cpf.equals("00000000000") || cpf.equals("11111111111") || cpf.equals("22222222222") 
                || cpf.equals("33333333333") || cpf.equals("44444444444") || cpf.equals("55555555555") 
                ||cpf.equals("66666666666") || cpf.equals("77777777777") ||cpf.equals("88888888888") 
                || cpf.equals("99999999999") ||
                (cpf.length() != 11)|| !tipo.equals("adm") && !tipo.equals("usr")){
            
            JOptionPane.showMessageDialog(null, "Verifique os campos e tente novamente...");
        }else{
            try{
                int escolha = JOptionPane.showConfirmDialog(null, "Deseja atualizar o usuário?", "Atualizar", JOptionPane.YES_NO_OPTION);
                switch (escolha){
                    case JOptionPane.YES_OPTION:
                        UsuarioGerenciar usuariogerenciar = new UsuarioGerenciar (id, tipo, login, senha, nome, cpf);
                        DAO dao = new DAO();
                        dao.atualizarUsuarios(usuariogerenciar);
                        JOptionPane.showMessageDialog(null, "Usuário atualizado com sucesso");
                        buscarUsuarios();
                        idTextField.setText("");
                        tipoTextField.setText("");
                        loginTextField.setText("");
                        senhaPasswordField.setText("");
                        nomeTextField.setText("");
                        cpfTextField.setText("");
                            break;
                    default:
                        JOptionPane.showMessageDialog(null, "O usuário não será atualizado.");
                            break;
                }
            }
            catch (Exception e){
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, "Sistema indisponível, tente novamente mais tarde...");
            }
        }   
    }//GEN-LAST:event_atualizarButtonActionPerformed

    private void removerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removerButtonActionPerformed
        int escolha = JOptionPane.showConfirmDialog(null, "Deseja remover usuário?", "Remover", JOptionPane.YES_NO_OPTION);
        if (escolha == JOptionPane.YES_OPTION){
            try{
                int id = Integer.parseInt (idTextField.getText());
                UsuarioGerenciar usuariogerenciar = new UsuarioGerenciar(id);
                DAO dao = new DAO();
                dao.removerUsuarios(usuariogerenciar);
                JOptionPane.showMessageDialog(null, "Usuário removido com sucesso!");
                buscarUsuarios();
                idTextField.setText("");
                tipoTextField.setText("");
                loginTextField.setText("");
                senhaPasswordField.setText("");
                nomeTextField.setText("");
                cpfTextField.setText("");
                }
                catch (Exception e){
                    JOptionPane.showMessageDialog(null, "Falha técnica. Tente novamente mais tarde...");
                e.printStackTrace();
            }
        }        
    }//GEN-LAST:event_removerButtonActionPerformed

